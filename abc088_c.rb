c = []

3.times do
  c << gets.chomp.split(" ").map(&:to_i)
end

ans = true

if c[0][0] - c[0][1] != c[1][0] - c[1][1] || c[1][0] - c[1][1] != c[2][0] - c[2][1] || c[2][0] - c[2][1] != c[0][0] - c[0][1]
  ans = false
end

if c[0][1] - c[0][2] != c[1][1] - c[1][2] || c[1][1] - c[1][2] != c[2][1] - c[2][2] || c[2][1] - c[2][2] != c[0][1] - c[0][2]
  ans = false
end

if c[0][0] - c[1][0] != c[0][1] - c[1][1] || c[0][1] - c[1][1] != c[0][2] - c[1][2] || c[0][2] - c[1][2] != c[0][0] - c[1][0]
  ans = false
end

if c[1][0] - c[2][0] != c[1][1] - c[2][1] || c[1][1] - c[2][1] != c[1][2] - c[2][2] || c[1][2] - c[2][2] != c[1][0] - c[2][0]
  ans = false
end

if ans
  puts("Yes")
else
  puts("No")
end
